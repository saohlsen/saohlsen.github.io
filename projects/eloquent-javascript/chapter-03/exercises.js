// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
// set the counter that starts at 0
  var counter = 0;
// set string to lower case, and change to an array, use forEach function
  string.toLowerCase().split('').forEach(function(element){
    element === character.toLowerCase() ? counter++: null;
    
  });
// return outcome counter
  return counter;
}


////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
// set counter to 0  
  var counter = 0;
  string.toLowerCase().split('').forEach(function(element){
    element === 'b' ? counter++:null;
  });
  return counter;
}


